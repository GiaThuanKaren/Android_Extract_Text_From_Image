  void GetConnectionSocket() {
    const URLServer = 'http://192.168.1.5:5000';
    socket = IO.io(
        URLServer,
        IO.OptionBuilder()
            .setTransports(['websocket']) // for Flutter or Dart VM
            .setExtraHeaders({'foo': 'bar'}) // optional
            .build());
    // Replace 'onConnect' with any of the above events.
    socket?.onConnect((_) {
      print('connect');
    });
    socket?.on("connecting", (data) {
      print("Connecting");
    });
    // socket.on("", (data) => null)
    socket?.on('connect_error', (data) {
      print(data);
    });
    socket?.onDisconnect((_) => print('disconnect'));
  }

  void getImage(ImageSource source) async {
    try {
      final pickedImage = await ImagePicker().pickImage(source: source);
      if (pickedImage != null) {
        textScanning = true;
        imageFile = pickedImage;
        setState(() {});
        getRecognisedText(pickedImage);
      }
    } catch (e) {
      textScanning = false;
      imageFile = null;
      scannedText = "Error occured while scanning";
      setState(() {});
    }
  }

  void getRecognisedText(XFile image) async {
    final inputImage = InputImage.fromFilePath(image.path);
    final textDetector = GoogleMlKit.vision.textDetector();
    RecognisedText recognisedText = await textDetector.processImage(inputImage);
    await textDetector.close();
    scannedText = "";
    for (TextBlock block in recognisedText.blocks) {
      for (TextLine line in block.lines) {
        scannedText = scannedText + line.text + "\n";
      }
    }

    print("Load Id Room");
    String idFromPre = Cache.instance.IdRoom;
    print("Id Room From Ref ${idFromPre}");
    socket?.emit('send-text', {scannedText,idFromPre});
    textScanning = false;
    setState(() {});
  }

